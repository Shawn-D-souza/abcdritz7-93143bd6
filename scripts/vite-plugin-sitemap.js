import fs from 'fs';
import path from 'path';

const BASE_URL = 'https://ritz7.ai';

// Static pages that are always in the sitemap
const STATIC_PAGES = [
  { loc: '/', priority: '1.00' },
  { loc: '/blogs/', priority: '0.80' },
  { loc: '/hall-of-fame/', priority: '0.80' },
  { loc: '/privacy-policy/', priority: '0.80' },
  { loc: '/community-guidelines/', priority: '0.80' },
];

/**
 * Parses the frontmatter of a markdown file and returns key fields.
 */
function parseFrontmatter(content) {
  const match = content.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return null;

  const frontmatter = match[1];

  const get = (key) => {
    const line = frontmatter.split('\n').find((l) => l.startsWith(key + ':'));
    if (!line) return null;
    return line.slice(key.length + 1).trim().replace(/^["']|["']$/g, '');
  };

  return {
    slug: get('slug'),
    updated_date: get('updated_date'),
    date: get('date'),
  };
}

/**
 * Formats a date string to W3C datetime format used in sitemaps.
 */
function formatDate(dateStr) {
  try {
    return new Date(dateStr).toISOString();
  } catch {
    return new Date().toISOString();
  }
}

/**
 * Builds the XML content for the sitemap.
 */
function buildSitemap(blogs) {
  const now = new Date().toISOString();

  const staticUrls = STATIC_PAGES.map(
    ({ loc, priority }) => `
<url>
  <loc>${BASE_URL}${loc}</loc>
  <lastmod>${now}</lastmod>
  <priority>${priority}</priority>
</url>`
  ).join('');

  const blogUrls = blogs.map(
    ({ slug, lastmod }) => `
<url>
  <loc>${BASE_URL}/blog/${slug}/</loc>
  <lastmod>${lastmod}</lastmod>
  <priority>0.80</priority>
</url>`
  ).join('');

  return `<?xml version="1.0" encoding="UTF-8"?>
<urlset
      xmlns="http://www.sitemaps.org/schemas/sitemap/0.9"
      xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance"
      xsi:schemaLocation="http://www.sitemaps.org/schemas/sitemap/0.9
            http://www.sitemaps.org/schemas/sitemap/0.9/sitemap.xsd">
<!-- Auto-generated by vite-plugin-sitemap during build -->
${staticUrls}
${blogUrls}
</urlset>
`;
}

/**
 * Vite plugin: generates public/sitemap.xml automatically at build time
 * by reading all markdown files in src/content/blog/.
 */
export function sitemapPlugin() {
  return {
    name: 'vite-plugin-sitemap',
    // Runs after the bundle is written to disk
    closeBundle() {
      const blogDir = path.resolve('src/content/blog');
      const outPath = path.resolve('public/sitemap.xml');

      if (!fs.existsSync(blogDir)) {
        console.warn('[sitemap] Blog directory not found:', blogDir);
        return;
      }

      const files = fs.readdirSync(blogDir).filter((f) => f.endsWith('.md'));

      const blogs = [];
      for (const file of files) {
        const content = fs.readFileSync(path.join(blogDir, file), 'utf-8');
        const meta = parseFrontmatter(content);
        if (!meta || !meta.slug) continue;

        blogs.push({
          slug: meta.slug,
          lastmod: formatDate(meta.updated_date || meta.date),
        });
      }

      // Sort newest-first (best practice for sitemaps)
      blogs.sort((a, b) => new Date(b.lastmod) - new Date(a.lastmod));

      const xml = buildSitemap(blogs);
      fs.writeFileSync(outPath, xml, 'utf-8');

      console.log(`[sitemap] ✅ Generated sitemap.xml with ${blogs.length} blog(s) + ${STATIC_PAGES.length} static page(s).`);
    },
  };
}
